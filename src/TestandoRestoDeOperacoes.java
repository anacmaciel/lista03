public class TestandoRestoDeOperacoes {
    public static void main(String[] args) {

        int numero1 = 9;
        int numero2 = 2;

        int operacaoDivisao = numero1 / numero2;
        System.out.println(operacaoDivisao);
        int restoOperacao = numero1 % numero2;
        System.out.println(restoOperacao);
    }
}
